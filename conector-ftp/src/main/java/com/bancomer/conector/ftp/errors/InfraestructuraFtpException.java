package com.bancomer.conector.ftp.errors;

/**
 * 
 * @author Sergio Mena.
 *
 */
public class InfraestructuraFtpException extends Exception {
	
    /**
     * Serial id.
     */
    private static final long serialVersionUID = 1L;
    
    /**
     * Constructor default
     */
    public InfraestructuraFtpException() {
        super();
    }

    /**
     * Constructor que recibe la cadena de error y una Throwable.
     * @param message Cadena de Error
     * @param cause Objeto Throwable
     */
    public InfraestructuraFtpException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * Constructor que recibe la cadena de error
     * @param message Cadena de error
     */
    public InfraestructuraFtpException(String message) {
        super(message);
    }

    /**
     * Constructor que recibe el objeto Throwable
     * @param cause Objeto Throwable
     */
    public InfraestructuraFtpException(Throwable cause) {
        super(cause);
    }

}
